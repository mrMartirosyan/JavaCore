package classwork.Lesson12.calculator;

public class Calculator {

    int plus(int x, int y) {
        return x + y;
    }

    int minus(int x, int y) {
        return x - y;
    }

    int multiple(int x, int y) {
        return x * y;
    }

    int divide(int x, int y) {
        return x / y;
    }
}
